import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from tensorflow.keras.models import Sequential
from sklearn.preprocessing import MinMaxScaler
from tensorflow.keras.layers import LSTM, Dense

data = pd.read_csv("/content/home_temperature_and_humidity_smoothed_filled.csv")

print(data.head())

dataset = data["humidity_exterieur"].values

print(dataset)

scaler = MinMaxScaler(feature_range=(0,1))
scaled_data = scaler.fit_transform(dataset.reshape(-1,1))
print(scaled_data)

X = []
Y = []
for i in range(30, len(scaled_data)):
  X.append(scaled_data[i-30:i, 0])
  Y.append(scaled_data[i,0])

X = np.array(X)
Y = np.array(Y)
X = np.reshape(X, (X.shape[0], X.shape[1], 1))
print("Shape of X is:", X.shape)

model = Sequential()
model.add(LSTM(units=50, return_sequences=True, input_shape=(X.shape[1],1)))
model.add(LSTM(50))
model.add(Dense(units=1))

model.compile(optimizer="adam", loss="mean_squared_error")

history = model.fit(X, Y, epochs=20, batch_size=32)

predicted_temperature = model.predict(X)

predicted_temperature=scaler.inverse_transform(predicted_temperature)

print(predicted_temperature)

real_temperature = scaler.inverse_transform(Y.reshape(-1,1))

print(real_temperature)

plt.figure(figsize=(12,6))

plt.plot(real_temperature,
         label='Real humidity')

plt.plot(predicted_temperature,
         label='Predicted humidity')

plt.title("Humidity Prediction Using LSTM")

plt.xlabel("Days")

plt.ylabel("Humidity")

plt.legend()

plt.show()

model.summary()

model.save("Humidity Prediction Using LSTM.h5")

